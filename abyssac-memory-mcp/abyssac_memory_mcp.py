class MCPWorkflowServer:
    """MCP工作流服务器"""
    
    def run(self):
        """运行服务器"""
        import sys
        for line in sys.stdin:
            try:
                request = json.loads(line)
                response = self.handle_request(request)
                print(json.dumps(response, ensure_ascii=False), flush=True)
            except Exception as e:
                print(json.dumps({
                    "jsonrpc": "2.0", 
                    "error": {"code": -32603, "message": str(e)}
                }, ensure_ascii=False), flush=True)
    
    def handle_request(self, request: dict) -> dict:
        """处理请求"""
        method = request.get("method", "")
        params = request.get("params", {})
        request_id = request.get("id")
        
        if method == "initialize":
            return {
                "jsonrpc": "2.0",
                "id": request_id,
                "result": {
                    "protocolVersion": "2024-11-05",
                    "capabilities": {"tools": {}},
                    "serverInfo": {
                        "name": "abyssac-memory-mcp",
                        "version": "7.0.0",
                        "description": "AbyssAc Memory MCP - 步骤级验证 + 回退机制"
                    }
                }
            }
        
        elif method == "tools/list":
            return {
                "jsonrpc": "2.0",
                "id": request_id,
                "result": {
                    "tools": [
                        {
                            "name": "mcp_instruction",
                            "description": "获取指定步骤的指令模板。步骤: ENTRY, CACHE_OPT, R1_1-R4_2, REVIEW_R, S1-S6_2, REVIEW_S, C1-C5, REVIEW_C, DONE",
                            "inputSchema": {
                                "type": "object",
                                "properties": {
                                    "step": {
                                        "type": "string", 
                                        "description": "步骤名称"
                                    }
                                },
                                "required": ["step"]
                            }
                        },
                        {
                            "name": "list_steps",
                            "description": "列出所有可用步骤",
                            "inputSchema": {
                                "type": "object",
                                "properties": {}
                            }
                        },
                        {
                            "name": "workflow_overview",
                            "description": "获取工作流概览",
                            "inputSchema": {
                                "type": "object",
                                "properties": {}
                            }
                        }
                    ]
                }
            }
        
        elif method == "tools/call":
            tool_name = params.get("name", "")
            tool_params = params.get("arguments", {})
            
            if tool_name == "mcp_instruction":
                step = tool_params.get("step", "")
                result = get_instruction(step)
            elif tool_name == "list_steps":
                result = {"steps": list_steps()}
            elif tool_name == "workflow_overview":
                result = get_workflow_overview()
            else:
                return {
                    "jsonrpc": "2.0",
                    "id": request_id,
                    "error": {"code": -32601, "message": f"Tool not found: {tool_name}"}
                }
            
            return {
                "jsonrpc": "2.0",
                "id": request_id,
                "result": {
                    "content": [{
                        "type": "text",
                        "text": json.dumps(result, ensure_ascii=False, indent=2)
                    }]
                }
            }
        
        return {
            "jsonrpc": "2.0",
            "id": request_id,
            "error": {"code": -32601, "message": f"Method not found: {method}"}
        }


def main():
    """主入口"""
    server = MCPWorkflowServer()
    server.run()


if __name__ == "__main__":
    main()
